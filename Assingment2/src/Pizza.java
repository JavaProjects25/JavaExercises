class Pizza {

    String dough;
    String sauce;
    String toppings;

    String getDough(){
        return dough;
    }

    String getSauce(){
        return sauce;
    }

    String getToppings(){
        return toppings;
    }

    void setDough(String dough){
        this.dough = dough;
    }

    void setSauce(String sauce){
        this.sauce = sauce;
    }

    void setToppings(String toppings){
        this.toppings = toppings;
    }
}
